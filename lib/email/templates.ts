function buildDealBlock(deal: Deal, rank: number, baseUrl: string): string {
  const scoreColor =
    (deal.composite_score ?? 0) >= 70
      ? 'score-high'
      : (deal.composite_score ?? 0) >= 50
      ? 'score-med'
      : 'score-low';

  const driver = buildScoreDriver(deal.score_breakdown);
  const detailUrl = `${baseUrl}/deals/${deal.id}`;

  return `
  <div class="deal-card">
    <div class="deal-header">
      <div class="deal-rank">#${rank} EXECUTABILITY PICK</div>
      <div class="deal-title">${esc(deal.title)}</div>
      <div class="meta-row">
        ${deal.sponsoring_state ? `<span class="badge badge-state">${esc(deal.sponsoring_state)}</span>` : ''}
        <span class="badge badge-sector">${formatSector(deal.sector)}</span>
        <span class="badge badge-stage">${formatStage(deal.lifecycle_stage)}</span>
      </div>
    </div>
    <div class="score-row">
      <div>
        <div class="score-main ${scoreColor}">${deal.composite_score?.toFixed(0) ?? '—'}</div>
        <div style="font-size:10px;color:#64748b;text-align:center">/ 100</div>
      </div>
      <div class="score-subscores">${buildSubScoreText(deal.score_breakdown)}</div>
    </div>
    <div class="deal-body">
      ${deal.rom_value_usd ? `<p class="rom"><strong>Size:</strong> ${formatRom(deal.rom_value_usd, deal.rom_value_min, deal.rom_value_max)} ${deal.rom_basis ? `— ${esc(deal.rom_basis)}` : ''}</p>` : ''}
      <div class="section-label">Executive Summary</div>
      <p class="summary">${esc(deal.executive_summary ?? 'Analysis pending.')}</p>
      ${driver ? `<p class="driver">Score driver: ${esc(driver)}</p>` : ''}
      <a href="${detailUrl}" class="cta">View Full Deal →</a>
    </div>
  </div>`;
}

function buildSubScoreText(breakdown: ScoreBreakdown | null | undefined): string {
  if (!breakdown) return 'Score breakdown not yet calculated';
  return [
    `Likelihood:    ${breakdown.likelihood.score}/100 (×${breakdown.likelihood.weight})`,
    `Actionability: ${breakdown.actionability.score}/100 (×${breakdown.actionability.weight})`,
    `Financing:     ${breakdown.financing.score}/100 (×${breakdown.financing.weight})`,
    `Corroboration: ${breakdown.corroboration.score}/100 (×${breakdown.corroboration.weight})`,
    `Strat. Priority: ${breakdown.strategic_priority.score}/100 (×${breakdown.strategic_priority.weight})`,
  ].join('\n');
}

function buildScoreDriver(breakdown: ScoreBreakdown | null | undefined): string {
  if (!breakdown) return '';
  const subs = [
    { name: 'Likelihood', s: breakdown.likelihood },
    { name: 'Actionability', s: breakdown.actionability },
    { name: 'Financing', s: breakdown.financing },
    { name: 'Corroboration', s: breakdown.corroboration },
    { name: 'Strategic Priority', s: breakdown.strategic_priority },
  ];
  const top = subs.sort((a, b) => b.s.contribution - a.s.contribution)[0];
  return top ? `${top.name} (${top.s.score}/100, weight ${top.s.weight}) — ${top.s.reasoning}` : '';
}

function esc(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
