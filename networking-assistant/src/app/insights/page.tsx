"use client";

import { useState } from "react";
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { Brain, RefreshCw, Loader2 } from "lucide-react";

export default function InsightsPage() {
  const [weeklyBrief, setWeeklyBrief] = useState<string | null>(null);
  const [generating, setGenerating] = useState(false);

  async function generateBrief() {
    setGenerating(true);
    try {
      const res = await fetch("/api/claude/weekly-brief", { method: "POST" });
      if (res.ok) {
        const data = await res.json();
        setWeeklyBrief(data.brief);
      }
    } catch (err) {
      console.error("Failed to generate brief:", err);
    }
    setGenerating(false);
  }

  return (
    <div className="p-8 max-w-4xl mx-auto space-y-6">
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-3xl font-bold text-zinc-900 dark:text-zinc-50">
            AI Insights
          </h1>
          <p className="text-zinc-500 mt-1">
            Claude-powered analysis of your network
          </p>
        </div>
        <Button onClick={generateBrief} disabled={generating}>
          {generating ? (
            <Loader2 className="h-4 w-4 animate-spin" />
          ) : (
            <RefreshCw className="h-4 w-4" />
          )}
          {generating ? "Analyzing..." : "Generate Weekly Brief"}
        </Button>
      </div>

      {generating && (
        <Card>
          <CardContent className="py-12 text-center">
            <Brain className="h-12 w-12 mx-auto text-zinc-400 animate-pulse" />
            <p className="text-zinc-500 mt-3">
              Claude (Opus) is analyzing your entire network...
            </p>
            <p className="text-xs text-zinc-400 mt-1">
              Reviewing relationships, identifying patterns, and generating
              recommendations
            </p>
          </CardContent>
        </Card>
      )}

      {weeklyBrief && !generating && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <Brain className="h-5 w-5" />
              State of Your Network
            </CardTitle>
            <CardDescription>
              Generated by Claude Opus — {new Date().toLocaleDateString()}
            </CardDescription>
          </CardHeader>
          <CardContent>
            <div
              className="prose prose-zinc dark:prose-invert max-w-none"
              dangerouslySetInnerHTML={{ __html: formatMarkdown(weeklyBrief) }}
            />
          </CardContent>
        </Card>
      )}

      {!weeklyBrief && !generating && (
        <Card>
          <CardContent className="py-16 text-center">
            <Brain className="h-16 w-16 mx-auto text-zinc-300 dark:text-zinc-700" />
            <h2 className="text-xl font-semibold mt-4 text-zinc-700 dark:text-zinc-300">
              Your network intelligence hub
            </h2>
            <p className="text-zinc-500 mt-2 max-w-md mx-auto">
              Generate a comprehensive analysis of your network. Claude will
              review all your contacts, recent interactions, and goals to
              provide actionable insights.
            </p>
            <Button onClick={generateBrief} className="mt-6">
              <Brain className="h-4 w-4" />
              Generate Your First Brief
            </Button>
          </CardContent>
        </Card>
      )}
    </div>
  );
}

function formatMarkdown(md: string): string {
  return md
    .replace(/^#### (.+)$/gm, "<h4>$1</h4>")
    .replace(/^### (.+)$/gm, "<h3>$1</h3>")
    .replace(/^## (.+)$/gm, "<h2>$1</h2>")
    .replace(/^# (.+)$/gm, "<h1>$1</h1>")
    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
    .replace(/\*(.+?)\*/g, "<em>$1</em>")
    .replace(/^- (.+)$/gm, "<li>$1</li>")
    .replace(/(<li>.*<\/li>\n?)+/g, "<ul>$&</ul>")
    .replace(/\n\n/g, "<br/><br/>");
}
